// this function gets called at startup 
function gol() {
	alert("game of life");  //<<< this is visibly the entry point of the program
	ctx = document.getElementById('gol').getContext('2d');
	ctx.canvas.height = grid.length *10;
	ctx.canvas.width  = grid[0].length * 10;
	setTimeout(time_step, 0); 	
}

// this function gets called at each time step 
function time_step() {
	display_grid(ctx, grid);
	grid = transform_grid(grid);
	setTimeout(time_step, 100); 	//< gives a bit of time for the browser to do his own work
}

// this function calculates which cells are alive after the time step.
// implementing it is a very good introduction to programming.
