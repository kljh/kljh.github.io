function transform_grid(grid) {
	// creates a new empty grid
	var n=grid.length; 
	var m=grid[0].length;
	var new_grid = new_empty_grid(n, m);
	
	// FROM HERE WRITE THE CODE FOR NEXT TIME STEP
	
	// for each cell decides if it alives
	new_grid[2][5] =1
	new_grid[2][6]=1
	new_grid[1][8]=1
	
	if (new_grid[2][5] == new_grid[1][2*2*2])
		new_grid[0][0]=1
	
	for(i=8;i<14;i++) {
		new_grid[1][i]=1
	}
		
	// loop on row
	for(i=7;i<15;i++) {
		// loop on columns 
		for(j=0;j<35;j++) {
			
			if  ( (i+j)%5 != 0)
				new_grid[i][j]=1;
		}
	}
	
	return new_grid;
}

// this function displays the cells
function display_grid(ctx, grid) {
	ctx.save();
	ctx.scale(1,1);
	var n=grid.length;
	var m=grid[0].length;
	for (var i=0; i<n; i++) {
		for (var j=0; j<m; j++) {
			var life = grid[i][j];
			if (life) {
				ctx.fillStyle = "blue";
			} else {
				ctx.fillStyle = "lightgrey";
			}
			var y = 10*i+1;
			var x = 10*j+1;
			ctx.fillRect (x, y, 8, 8);
		}
	}
	ctx.restore();
}

// this function creates a new empty grid
function new_empty_grid(n, m) {
	var new_grid = new Array(n);
	for (var i=0; i<n; i++) 
		new_grid[i]  = new Array(m);
	return new_grid;
}
